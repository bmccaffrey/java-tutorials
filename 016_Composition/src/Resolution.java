/**
 * Resolution
 */
public class Resolution {
  // Fields
  private int width;
  private int height;

  // Constructor
  public Resolution(int width, int height) {
    this.width = width;
    this.height = height;
  }

  // Methods
  public int getWidth() {
    return this.width;
  }

  public int getHeight() {
    return this.height;
  }

}